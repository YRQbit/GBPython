#
# Пример:
#   - Python ==>> Введите число месяца:
#   - Пользователь вводит ==>> 1
#   - Python ==>> январь
#
#   - Python ==>> Введите число месяца:
#   - Пользователь вводит ==>> 3
#   - Python ==>> март
