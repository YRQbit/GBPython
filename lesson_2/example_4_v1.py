# Задачи:
# 1. Вывести в консоль запрос на ввод произвольной строки с пробелами: "Введите произвольную строку с пробелами:";
# 2. Разбить фразу на слова (разделителем принять символ пробела);
# 3. Проверить количество символов в каждом слове
#   - в случае, когда символов в слове больше 10, получить только первые 10;
# 4. Вывести каждое полученное слово в консоли на новой строке;
# 5. Вывести в консоль полученное из Списка значение месяца;
# 6. Выбрать из Словаря месяц, соответствующий числовому значению, полученному от Пользователя:
# 7. Вывести в консоль полученное из Словаря значение месяца;
# .


us_str = str(input("Введите произвольную строку с пробелами: "))

str_list = us_str.split(" ")
for count, str_word in enumerate(str_list):

    if len(str_word) > 10:

        print(count+1,str_word[:10])

    else:
        print(count+1,str_word)
